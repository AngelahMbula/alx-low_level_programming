#include "main.h"
#include <stdio.h>

/**
 * print_array - prints n elements of an array of integers
 * @a: first integer
 * @n: second integer
 * Return: void
 */

void print_array(int *a, int n)
{
	int i;

	for (a = 0; a < i; a++)
	{
		i[a] = a + 100;
	}
	for (n = 0; n < i; n++)
	{
		printf("Element [%d] = %d\n", n, i[n]);
		printf(", ");
	}
	return (0);
}
